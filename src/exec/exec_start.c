int	handle_keypress(int keycode, t_ray *rc)
{
	if (keycode == 65307)
	{
		free_all(rc);
		exit(0);
	}
	if (keycode == 119)
		rc->move_forward = 1;
	if (keycode == 115)
		rc->move_backward = 1;
	if (keycode == 100)
		rc->move_right = 1;
	if (keycode == 97)
		rc->move_left = 1;
	if (keycode == 65361)
		rc->cam_left = 1;
	if (keycode == 65363)
		rc->cam_right = 1;
	return (0);
}

int	handle_keyrelease(int keycode, t_ray *rc)
{
	if (keycode == 119)
		rc->move_forward = 0;
	if (keycode == 115)
		rc->move_backward = 0;
	if (keycode == 100)
		rc->move_right = 0;
	if (keycode == 97)
		rc->move_left = 0;
	if (keycode == 65361)
		rc->cam_left = 0;
	if (keycode == 65363)
		rc->cam_right = 0;
	return (0);
}

void	load_texture(t_ray *rc, t_texture *texture, char *path)
{
	texture->img = mlx_xpm_file_to_image(rc->mlx, path, \
	&texture->width, &texture->height);
	if (!texture->img)
		exit(EXIT_FAILURE);
	texture->addr = mlx_get_data_addr(texture->img, &texture->bits_per_pixel, \
	&texture->line_length, &texture->endian);
}

void	load_textures(t_ray *rc)
{
	load_texture(rc, &rc->textures[0], "north.xpm");
	load_texture(rc, &rc->textures[1], "south.xpm");
	load_texture(rc, &rc->textures[2], "east.xpm");
	load_texture(rc, &rc->textures[3], "west.xpm");
}

t_ray	*init_raycasting(void)
{
	t_ray	*rc;

	rc = malloc(sizeof(t_ray));
	rc->mlx = mlx_init();
	rc->win_width = 1600;
	rc->win_height = 1200;
	rc->win = mlx_new_window(rc->mlx, rc->win_width, rc->win_height, "Cub3D");
	rc->pos_x = 2;// a changer selon la pos du j au debut
	rc->pos_y = 2;// a changer selon la pos du j au debut
	rc->dir_x = -1;// a changer selon la dir (N,W,S,E) du j au debut
	rc->dir_y = 0;// a changer selon la dir (N,W,S,E) du j au debut
	rc->plane_x = 0;// le FOV
	rc->plane_y = 0.66; // le FOV
	rc->map = create_map();
	rc->move_left = 0;
	rc->move_right = 0;
	rc->move_forward = 0;
	rc->move_backward = 0;
	rc->cam_left = 0;
	rc->cam_right = 0;
	load_textures(rc);
	return (rc);
}

void	start_exec(void)
{
	t_ray	*rc;

	rc = init_raycasting();
	mlx_hook(rc->win, 2, 1L << 0, handle_keypress, rc);
	mlx_hook(rc->win, 3, 1L << 1, handle_keyrelease, rc);
	mlx_loop_hook(rc->mlx, (int (*)(void *))update_frame, rc);
	mlx_loop(rc->mlx);
}
